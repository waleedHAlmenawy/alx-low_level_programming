#include "3-calc.h"
/**
 * op_add - Adds two numbers
 * @a: First number
 * @b: Second number
 * Return: Result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Subs two numbers
 * @a: First number
 * @b: Second number
 * Return: Result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Muls two numbers
 * @a: First number
 * @b: Second number
 * Return: Result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Divs two numbers
 * @a: First number
 * @b: Second number
 * Return: Result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Mods two numbers
 * @a: First number
 * @b: Second number
 * Return: Result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
